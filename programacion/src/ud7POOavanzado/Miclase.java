package ud7POOavanzado;

public class Miclase {
	private int num;
	private String cad;
	public Miclase (int num, String cad){
		this.num = num;
		this.cad = cad;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getCad() {
		return cad;
	}
	public void setCad(String cad) {
		this.cad = cad;
	}
	
	
 
}
